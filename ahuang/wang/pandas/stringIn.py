# 定义一个字符串
text = "Hello, welcome to the world of Python."

# 检查字符串中是否包含子串"welcome"
is_welcome = "welcome" in text

# 显示检查结果
print("Does the text contain 'welcome'?", is_welcome)

# 检查字符串中是否包含子串"Python"
is_python = "Python" in text

# 显示检查结果
print("Does the text contain 'Python'?", is_python)

# 检查字符串中是否包含子串"Java"
is_java = "Java" in text

# 显示检查结果
print("Does the text contain 'Java'?", is_java)