# 定义一个字符串列表
words = ["Hello", "world", "this", "is", "Python"]

# 使用' '.join()方法将列表中的字符串连接成一个单一的字符串
sentence = ' '.join(words)

# 显示连接后的结果
print("Joined String:", sentence)

# 定义一个元组，包含不同格式的日期部分
date_parts = ("2024", "05", "19")

# 使用'-'.join()方法将元组中的字符串连接成一个日期字符串
date_string = '-'.join(date_parts)

# 显示连接后的日期字符串
print("Joined Date String:", date_string)